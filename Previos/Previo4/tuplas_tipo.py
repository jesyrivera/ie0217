#diferentes tipos de tuplas
#tupla vacia
my_tuple = ()
#imprime la tupla
print(my_tuple)

#tupla que tiene int
my_tuple = (1, 2, 3)
#imprime la tupla
print(my_tuple)

#tupla mixta
my_tuple = (1, "Hello", 3.4)
#imprimir tupla
print(my_tuple)

#nested tupla
my_tuple = ("mouse", [8, 4, 6], (1, 2, 3))
#imprimir la tupla
print(my_tuple)